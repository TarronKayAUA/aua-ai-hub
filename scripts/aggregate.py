def post_process_summary(text: str, fallback: str, word_cap: int) -> str:
    """Enforce site style on an LLM summary: no em dashes, no markup, word cap.

    Falls back to the feed's own summary when the model returned nothing usable.
    """
    text = strip_html(text or "")
    text = re.sub(r"[*_`#>\[\]()]+", " ", text)  # strip markdown leftovers
    text = remove_dashes(re.sub(r"\s+", " ", text).strip())
    words = text.split()
    if len(words) > word_cap:
        text = " ".join(words[:word_cap]).rstrip(",;:") + "."
    if not text:
        return fallback
    return text


# Token usage from the most recent Anthropic call, for the cost line on
# paid tasks (the pipeline is single-threaded, so one slot is enough).
LAST_ANTHROPIC_USAGE: dict = {}


def call_anthropic(system: str, user: str, cfg: dict, timeout: int) -> str:
    headers = {
        "x-api-key": os.environ["ANTHROPIC_API_KEY"],
        "anthropic-version": cfg["api_version"],
        "content-type": "application/json",
    }
    body = {
        "model": cfg["model"],
        "max_tokens": int(cfg.get("max_tokens", 4000)),
        "system": system,
        "messages": [{"role": "user", "content": user}],
    }
    # Fable-class models: safety classifiers can decline a benign-adjacent
    # request (stop_reason "refusal"); the server-side fallback re-serves
    # the call on the named fallback model inside the same request, so the
    # weekly artifact still ships. Configured per task in feeds.yaml.
    if cfg.get("fallback_model"):
        headers["anthropic-beta"] = "server-side-fallback-2026-06-01"
        body["fallbacks"] = [{"model": cfg["fallback_model"]}]
    resp = requests.post(cfg["endpoint"], headers=headers, json=body,
                         timeout=timeout)
    resp.raise_for_status()
    data = resp.json()
    LAST_ANTHROPIC_USAGE.clear()
    LAST_ANTHROPIC_USAGE.update(data.get("usage") or {})
    if data.get("stop_reason") == "refusal":
        # Double refusal (primary and fallback both declined) or no
        # fallback configured: return empty so the caller's validator and
        # retry machinery treat it as a rejected draft, never as content.
        return ""
    # Join text blocks only: thinking-enabled models (always-on for
    # Fable-class) prepend thinking blocks that must not reach the page.
    return "".join(
        b["text"] for b in data["content"] if b.get("type") == "text")


def call_github_models(system: str, user: str, cfg: dict, timeout: int) -> str:
    resp = requests.post(
        cfg["endpoint"],
        headers={
            "Authorization": f"Bearer {os.environ['GITHUB_TOKEN']}",
            "Accept": "application/vnd.github+json",
            "X-GitHub-Api-Version": cfg["api_version"],
            "Content-Type": "application/json",
        },
        json={
            "model": cfg["model"],
            "max_tokens": 4000,
            "messages": [
                {"role": "system", "content": system},
                {"role": "user", "content": user},
            ],
        },
        timeout=timeout,
    )
    resp.raise_for_status()
    return resp.json()["choices"][0]["message"]["content"]


def resolve_task_llm(config: dict, task: str):
    """Per-task provider resolution (owner spend plan, 2026-06-11): the
    Anthropic key activates only the tasks that name anthropic in the
    feeds.yaml llm.tasks block; every task falls back to the free GitHub
    Models path when the key is absent. Returns (provider, call_fn, cfg),
    or (None, None, None) when no LLM credential is available."""
    llm_cfg = config["llm"]
    spec = llm_cfg.get("tasks", {}).get(task, {})
    if (spec.get("provider") == "anthropic"
            and os.environ.get("ANTHROPIC_API_KEY")):
        cfg = dict(llm_cfg["anthropic"])
        for key in ("model", "fallback_model", "max_tokens"):
            if spec.get(key):
                cfg[key] = spec[key]
        return "anthropic", call_anthropic, cfg
    if os.environ.get("GITHUB_TOKEN"):
        return "github_models", call_github_models, dict(llm_cfg["github_models"])
    return None, None, None


def parse_curator_json(text: str, candidate_ids: set, categories: dict,
                       strict_coverage: bool = True) -> tuple[list[dict], list[dict]]:
    """Parse and validate the strict JSON contract. Returns (keeps, drops):
    keeps as decision dicts, drops as {"id", "reason"} for the audit trail.
    The contract requires one entry per candidate; with strict_coverage a
    shortfall raises ValueError so the corrective retry fires, otherwise
    unaccounted ids become drops with a placeholder reason (so a model that
    truncates its response can never silently discard candidates again).
    Raises ValueError on any other problem."""
    text = text.strip()
    text = re.sub(r"^```(?:json)?\s*|\s*```$", "", text)  # tolerate code fences
    data = json.loads(text)
    items = data["items"]
    if not isinstance(items, list):
        raise ValueError("items is not a list")
    keeps: list[dict] = []
    drops: list[dict] = []
    seen_ids: set = set()
    for entry in items:
        item_id = str(entry["id"])
        if item_id not in candidate_ids:
            raise ValueError(f"unknown candidate id {item_id!r}")
        if item_id in seen_ids:
            raise ValueError(f"duplicate entry for candidate id {item_id!r}")
        seen_ids.add(item_id)
        if not entry.get("keep"):
            reason = clean_text(str(entry.get("reason", "")), 160)
            drops.append({"id": item_id, "reason": reason or "(no reason given)"})
            continue
        category = entry.get("category", "")
        if category not in categories and category not in ("videos", "podcasts"):
            raise ValueError(f"unknown category {category!r}")
        keeps.append(
            {
                "id": item_id,
                "category": category,
                "summary": str(entry.get("summary", "")),
                "importance": int(entry.get("importance", 3)),
                "is_cfp": bool(entry.get("is_cfp", False)),
            }
        )
    missing = candidate_ids - seen_ids
    if missing:
        if strict_coverage:
            raise ValueError(
                f"response omitted {len(missing)} of {len(candidate_ids)} candidates"
            )
        drops.extend(
            {"id": mid, "reason": "(not accounted for by the model)"}
            for mid in sorted(missing)
        )
    return keeps, drops


def _pack_candidates(ordered: list, budget: int, verbose: bool):
    """Greedy payload packing under the provider's character budget.
    GitHub Models' free tier caps requests at 8,000 tokens, so candidates
    beyond the budget simply are not curated this run."""
    by_id = {}
    entries = []
    used = 200  # wrapper overhead allowance
    for idx, item in enumerate(ordered):
        entry = {
            "id": str(idx),
            "feed_category": item.category,
            "source": item.source,
            "title": item.title[:140],
            "summary": item.summary[:100],
        }
        entry_len = len(json.dumps(entry, ensure_ascii=False))
        if used + entry_len > budget:
            break
        used += entry_len
        by_id[str(idx)] = item
        entries.append(entry)
    if verbose and len(entries) < len(ordered):
        print(f"  llm payload budget: {len(entries)} of {len(ordered)} "
              "candidates sent")
    return by_id, json.dumps({"candidates": entries}, ensure_ascii=False)


def curate_llm(fresh: list, config: dict, verbose: bool):
    """Batched news curation call. Returns (kept_by_category, cfp_items, note)
    or None when the LLM path failed and keyword mode should run instead."""
    llm_cfg = config["llm"]
    categories = {k: v["label"] for k, v in config["categories"].items()}
    timeout = llm_cfg["request_timeout_seconds"]

    system = CURATOR_PROMPT_PATH.read_text(encoding="utf-8")
    provider, call, cfg = resolve_task_llm(config, "curation")
    if call is None:
        return None

    ordered = sorted(fresh, key=lambda i: -i.score)[: llm_cfg["max_candidates"]]
    by_id, payload = _pack_candidates(
        ordered, cfg.get("max_payload_chars", 22000), verbose
    )
    # audit trail: candidates the model never saw are drops with a stated cause
    offered = {id(i) for i in by_id.values()}
    for item in fresh:
        if id(item) not in offered:
            item.drop_reason = "(not offered to the model: candidate or payload budget)"

    parsed = None
    for attempt, user_msg in enumerate(
        [
            payload,
            payload + "\n\nYour previous response was not valid JSON matching "
            "the contract. Respond again with only the JSON object.",
        ]
    ):
        try:
            raw = call(system, user_msg, cfg, timeout)
            parsed = parse_curator_json(
                raw, set(by_id), categories, strict_coverage=(attempt == 0)
            )
            break
        except (requests.RequestException, KeyError) as exc:
            # transport or schema error: no point retrying with a corrective
            # instruction, fall back to keyword mode immediately
            if verbose:
                print(f"  llm call failed: {type(exc).__name__}: {exc}")
            return None
        except (ValueError, json.JSONDecodeError) as exc:
            if verbose:
                print(f"  llm json invalid (attempt {attempt + 1}): {exc}")
    if parsed is None:
        return None
    decisions, model_drops = parsed
    for d in model_drops:
        item = by_id[d["id"]]
        item.drop_reason = d["reason"]
        if verbose:
            print(f"  drop: {item.source}: {item.title[:60]} | {d['reason']}")

    news_decisions = [d for d in decisions if d["category"] != "videos"]
    news_decisions.sort(key=lambda d: -d["importance"])
    news_decisions = news_decisions[: llm_cfg["max_keep"]]
    # model keeps discarded by code carry an audit reason too
    final_ids = {d["id"] for d in news_decisions}
    for d in decisions:
        if d["id"] not in final_ids:
            why = ("model assigned it to videos in the news call"
                   if d["category"] == "videos" else "over the news keep cap")
            by_id[d["id"]].drop_reason = f"(kept by the model, discarded: {why})"

    kept_by_category = {k: [] for k in categories}
    cfp_items = []
    for decision in news_decisions:
        item = by_id[decision["id"]]
        item.category = decision["category"]
        item.summary = post_process_summary(
            decision["summary"], item.summary, llm_cfg["summary_word_cap"]
        )
        item.score = float(decision["importance"])
        kept_by_category[decision["category"]].append(item)
        if decision["is_cfp"]:
            cfp_items.append(item)
    for pool in kept_by_category.values():
        pool.sort(key=lambda i: (-i.score, -i.published.timestamp()))
    note = f"llm ({provider}, {cfg['model']})"
    return kept_by_category, cfp_items, note


def curate_llm_media(fresh_media: list, media_label: str, max_keep: int,
                     config: dict, verbose: bool):
    """Separate focused curation call for one media type (videos or
    podcasts). Returns (kept_items, cfp_items) or None on failure
    (keyword fallback)."""
    llm_cfg = config["llm"]
    categories = {k: v["label"] for k, v in config["categories"].items()}
    timeout = llm_cfg["request_timeout_seconds"]
    system = CURATOR_PROMPT_PATH.read_text(encoding="utf-8")
    provider, call, cfg = resolve_task_llm(config, "curation")
    if call is None:
        return None

    ordered = sorted(fresh_media, key=lambda i: -i.published.timestamp())[:60]
    by_id, payload = _pack_candidates(
        ordered, cfg.get("max_payload_chars", 22000), verbose
    )
    # audit trail: candidates the model never saw are drops with a stated cause
    offered = {id(i) for i in by_id.values()}
    for item in fresh_media:
        if id(item) not in offered:
            item.drop_reason = "(not offered to the model: candidate or payload budget)"

    parsed = None
    for attempt, user_msg in enumerate(
        [
            payload,
            payload + "\n\nYour previous response was not valid JSON matching "
            "the contract. Respond again with only the JSON object.",
        ]
    ):
        try:
            raw = call(system, user_msg, cfg, timeout)
            parsed = parse_curator_json(
                raw, set(by_id), categories, strict_coverage=(attempt == 0)
            )
            break
        except (requests.RequestException, KeyError) as exc:
            if verbose:
                print(f"  video llm call failed: {type(exc).__name__}: {exc}")
            return None
        except (ValueError, json.JSONDecodeError) as exc:
            if verbose:
                print(f"  {media_label} llm json invalid "
                      f"(attempt {attempt + 1}): {exc}")
    if parsed is None:
        return None
    decisions, model_drops = parsed
    for d in model_drops:
        item = by_id[d["id"]]
        item.drop_reason = d["reason"]
        if verbose:
            print(f"  drop: {item.source}: {item.title[:60]} | {d['reason']}")

    # Every candidate in this call is the same media type, so any kept
    # decision belongs to it regardless of the category label the model
    # wrote back.
    if verbose:
        print(f"  {media_label} llm: {len(by_id)} candidates sent, "
              f"{len(decisions)} kept by model")
    decisions.sort(key=lambda d: -d["importance"])
    cut = decisions[max_keep:]
    for d in cut:
        by_id[d["id"]].drop_reason = "(kept by the model, cut by the keep cap)"
    decisions = decisions[:max_keep]
    kept_items = []
    cfp_items = []
    for decision in decisions:
        item = by_id[decision["id"]]
        item.category = media_label
        item.score = float(decision["importance"])
        # Curator-written description shown under the card; when the model
        # returns nothing usable, show no description rather than the raw
        # feed description snippet.
        item.summary = post_process_summary(
            decision["summary"], "", llm_cfg["summary_word_cap"]
        )
        kept_items.append(item)
        if decision["is_cfp"]:
            cfp_items.append(item)
    kept_items.sort(key=lambda i: -i.published.timestamp())
    return kept_items, cfp_items


def append_conference_flags(cfp_items: list, now: datetime, dry_run: bool) -> int:
    """Append CFP-flagged items for owner review. The pipeline never edits
    data/conferences.yaml itself; conference data stays human-in-the-loop."""
    if not cfp_items:
        return 0
    lines = []
    if not CONFERENCE_FLAGS_PATH.exists():
        lines.append("<!-- GENERATED by scripts/aggregate.py. Owner review "
                     "queue for CFP-flagged news items. -->")
        lines.append("# Conference flags")
        lines.append("")
    for item in cfp_items:
        lines.append(
            f"- {now.date().isoformat()}: [{md_escape(item.title)}]({item.url}) "
            f"({item.source}) {item.summary}"
        )
    if dry_run:
        print(f"[dry-run] would append {len(cfp_items)} items to "
              f"data/conference_flags.md")
    else:
        with CONFERENCE_FLAGS_PATH.open("a", encoding="utf-8", newline="\n") as fh:
            fh.write("\n".join(lines) + "\n")
    return len(cfp_items)


