@dataclass
class Item:
    title: str
    url: str
    source: str
    published: datetime
    summary: str
    category: str
    weight: float = 1.0
    score: float = 0.0
    first_seen: str = ""
    thumbnail: str = ""
    drop_reason: str = ""

    def to_ledger(self, kept: bool, now_iso: str) -> dict:
        record = {
            "url": self.url,
            "title_hash": title_hash(self.title),
            "first_seen": now_iso,
            "kept": kept,
        }
        if not kept and self.drop_reason:
            # audit trail: why the curator (or the code path) declined this
            record["reason"] = self.drop_reason
        if kept:
            record.update(
                title=self.title,
                source=self.source,
                published=self.published.isoformat(),
                summary=self.summary,
                category=self.category,
                score=round(self.score, 2),
            )
            if self.thumbnail:
                record["thumbnail"] = self.thumbnail
        return record


# --- text utilities ---------------------------------------------------------


def strip_html(text: str) -> str:
    text = re.sub(r"<[^>]+>", " ", text)
    text = html.unescape(text)
    return re.sub(r"\s+", " ", text).strip()


def remove_dashes(text: str) -> str:
    """Site style rule: no em dashes in rendered copy (CLAUDE.md)."""
    text = re.sub(r"(?<=\d)[–—](?=\d)", "-", text)  # numeric ranges: 2024-2025
    text = re.sub(r"\s*[–—]+\s*", ", ", text)
    return text


def clean_text(text: str, limit: int = 500) -> str:
    text = remove_dashes(strip_html(text))
    if len(text) > limit:
        text = text[: limit - 1].rsplit(" ", 1)[0] + "…"
    return text


def title_hash(title: str) -> str:
    normalized = re.sub(r"[^a-z0-9]+", "", title.lower())
    return hashlib.sha256(normalized.encode()).hexdigest()[:32]


def canonicalize(url: str) -> str:
    parts = urlsplit(url.strip())
    query = [
        (k, v)
        for k, v in parse_qsl(parts.query, keep_blank_values=True)
        if not k.lower().startswith("utm_") and k.lower() not in TRACKING_PARAMS
    ]
    path = parts.path.rstrip("/") or "/"
    return urlunsplit(
        (parts.scheme.lower(), parts.netloc.lower(), path, urlencode(query), "")
    )


def md_escape(text: str) -> str:
    return text.replace("[", r"\[").replace("]", r"\]")


def fmt_date(dt: datetime) -> str:
    return dt.strftime("%B %d, %Y").replace(" 0", " ")


