def fetch_feed(url: str, timeout: int, verbose: bool) -> bytes:
    last_error = None
    for attempt in range(1 + RETRIES):
        try:
            resp = requests.get(
                url, headers={"User-Agent": USER_AGENT}, timeout=timeout
            )
            if resp.status_code >= 400:
                raise requests.HTTPError(f"HTTP {resp.status_code}")
            return resp.content
        except requests.RequestException as exc:
            last_error = exc
            if verbose:
                print(f"    attempt {attempt + 1} failed: {exc}")
            if attempt < RETRIES:
                time.sleep(2)
    raise RuntimeError(str(last_error))


OG_IMAGE_RE = re.compile(
    r'<meta[^>]+(?:property|name)=["\'](?:og:image(?::url)?|twitter:image)'
    r'["\'][^>]+content=["\']([^"\']+)["\']',
    re.I,
)
OG_IMAGE_RE_REVERSED = re.compile(
    r'<meta[^>]+content=["\']([^"\']+)["\'][^>]+(?:property|name)='
    r'["\'](?:og:image(?::url)?|twitter:image)["\']',
    re.I,
)


def fetch_article_thumbnail(url: str, timeout: int = 10) -> str:
    """Best-effort og:image extraction from an article page. Returns "" on
    any failure; thumbnails are decoration, never worth failing a run for."""
    try:
        resp = requests.get(
            url,
            headers={"User-Agent": (
                "Mozilla/5.0 (Windows NT 10.0; Win64; x64) "
                "AppleWebKit/537.36 (KHTML, like Gecko) "
                "Chrome/126.0 Safari/537.36")},
            timeout=timeout,
        )
        if resp.status_code != 200:
            return ""
        head = resp.text[:60000]
        match = OG_IMAGE_RE.search(head) or OG_IMAGE_RE_REVERSED.search(head)
        if not match:
            return ""
        image = html.unescape(match.group(1)).strip()
        if image.startswith("//"):
            image = "https:" + image
        if not image.startswith("http"):
            return ""
        return image
    except requests.RequestException:
        return ""


def parse_entry_date(entry) -> datetime | None:
    for key in ("published_parsed", "updated_parsed"):
        struct = entry.get(key)
        if struct:
            return datetime(*struct[:6], tzinfo=timezone.utc)
    for key in ("published", "updated", "date"):
        raw = entry.get(key)
        if raw:
            try:
                parsed = dateparser.parse(raw)
            except (ValueError, OverflowError):
                continue
            if parsed:
                if parsed.tzinfo is None:
                    parsed = parsed.replace(tzinfo=timezone.utc)
                return parsed.astimezone(timezone.utc)
    return None


def normalize(parsed, source: str, category: str, weight: float, max_items: int,
              counters: dict) -> list[Item]:
    items = []
    for entry in parsed.entries:
        link = entry.get("link")
        title = clean_text(entry.get("title", ""), limit=300)
        if not link or not title:
            counters["no_link_or_title"] += 1
            continue
        published = parse_entry_date(entry)
        if published is None:
            counters["no_date"] += 1
            continue
        summary_raw = entry.get("summary") or entry.get("description") or ""
        if not summary_raw and entry.get("content"):
            summary_raw = entry["content"][0].get("value", "")
        thumbnail = ""
        media = entry.get("media_thumbnail") or entry.get("media_content")
        if media and media[0].get("url", "").startswith("http"):
            thumbnail = media[0]["url"]
        items.append(
            Item(
                title=title,
                url=canonicalize(link),
                source=source,
                published=published,
                summary=clean_text(summary_raw),
                category=category,
                weight=weight,
                thumbnail=thumbnail,
            )
        )
    items.sort(key=lambda i: i.published, reverse=True)
    return items[:max_items]


# --- videos -------------------------------------------------------------------


def normalize_videos(parsed, channel: str, counters: dict) -> list[Item]:
    items = []
    for entry in parsed.entries:
        link = entry.get("link")
        title = clean_text(entry.get("title", ""), limit=300)
        if not link or not title:
            counters["no_link_or_title"] += 1
            continue
        if "/shorts/" in link:
            continue  # Shorts are not a fit for the curated video page
        published = parse_entry_date(entry)
        if published is None:
            counters["no_date"] += 1
            continue
        thumbnail = ""
        media = entry.get("media_thumbnail")
        if media:
            thumbnail = media[0].get("url", "")
        elif entry.get("yt_videoid"):
            thumbnail = f"https://i.ytimg.com/vi/{entry['yt_videoid']}/hqdefault.jpg"
        description = entry.get("summary", "")
        if entry.get("media_description"):
            description = entry["media_description"]
        items.append(
            Item(
                title=title,
                url=canonicalize(link),
                source=channel,
                published=published,
                summary=clean_text(description, limit=150),
                category="videos",
                thumbnail=thumbnail,
            )
        )
    items.sort(key=lambda i: i.published, reverse=True)
    return items


def curate_media_keyword(fresh_media: list, max_keep: int) -> list:
    """Fallback selection: newest first, at most two per channel or show so
    one prolific source cannot fill the whole budget."""
    per_source: dict[str, int] = {}
    kept = []
    for item in sorted(fresh_media, key=lambda i: -i.published.timestamp()):
        if per_source.get(item.source, 0) >= 2:
            continue
        per_source[item.source] = per_source.get(item.source, 0) + 1
        # No curator-written description in fallback mode; raw feed
        # descriptions are too noisy to publish.
        item.summary = ""
        kept.append(item)
        if len(kept) >= max_keep:
            break
    return kept


def normalize_podcasts(parsed, show: str, counters: dict) -> list[Item]:
    art = ""
    image = parsed.feed.get("image")
    if image:
        art = image.get("href", "")
    items = []
    for entry in parsed.entries:
        link = entry.get("link")
        if not link and entry.get("links"):
            enclosures = [l.get("href") for l in entry["links"]
                          if l.get("rel") == "enclosure" and l.get("href")]
            link = enclosures[0] if enclosures else None
        title = clean_text(entry.get("title", ""), limit=300)
        if not link or not title:
            counters["no_link_or_title"] += 1
            continue
        published = parse_entry_date(entry)
        if published is None:
            counters["no_date"] += 1
            continue
        episode_art = art
        media = entry.get("image")
        if media and media.get("href"):
            episode_art = media["href"]
        items.append(
            Item(
                title=title,
                url=canonicalize(link),
                source=show,
                published=published,
                summary=clean_text(entry.get("summary", ""), limit=300),
                category="podcasts",
                thumbnail=episode_art,
            )
        )
    items.sort(key=lambda i: i.published, reverse=True)
    return items


def split_video_groups(records: list[dict], video_cfg: dict):
    """Order-preserving split of kept videos into the Videos page sections
    configured in feeds.yaml. A video's group comes from its channel's
    `group` key (owner-assigned, default general), never from model labels,
    which are unreliable for media (CLAUDE.md). Returns [(label, records)]
    with per-group caps applied; empty groups are dropped. Without a groups
    config the whole list is one unlabeled section capped by page_items."""
    groups = video_cfg.get("groups")
    if not groups:
        return [(None, records[: int(video_cfg.get("page_items", 24))])]
    by_source = {ch["name"]: ch.get("group", "general")
                 for ch in video_cfg.get("channels", [])}
    sections = []
    for key, gcfg in groups.items():
        members = [r for r in records
                   if by_source.get(r["source"], "general") == key]
        members = members[: int(gcfg.get("page_items", 20))]
        if members:
            sections.append((gcfg.get("label", key.title()), members))
    return sections


